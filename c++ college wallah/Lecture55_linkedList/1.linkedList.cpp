// LINKED LIST
// linear data structure used to store a list of values.

// arrays-> a single memory block with partitions.
// linked list-> memory blocks linked to each other.

// Challenges of array
// ->static sizes
// ->contiguous memory allocation

// Advantages of a linked list over an array
// ->dynamic size
// ->non-contiguous memory allocation
// ->insertion & deletion is non expensive

// Listnode-> blocks of memory->node
// node will have 2 parts data & next.

// Types of linked lists
// *singly linked list-->> every node points to its successor node.
// *Doubly linked list-->> every node is connected to its previous & next node.
// *Circular linked list-->>the last node point to head node.

